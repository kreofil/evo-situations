//==============================================================================
// coord-container-out.c - содержит процедуру
// вывода содержимого контейнера
//==============================================================================

#include <stdio.h>
#include "coord-container.h"

//------------------------------------------------------------------------------
// Сигнатуры требуемых функций можно тоже подключить через
// заголовочный файл. Но, для простоты, можно и так описать.
void CoordFigureOut(struct CoordFigure* s, FILE* ofst);

//------------------------------------------------------------------------------
// Вывод содержимого контейнера в указанный поток
void CoordContainerOut(CoordContainer *c, FILE* ofst) {
  fprintf(ofst, "Container contains %d elements.\n", c->len);
  for(int i = 0; i < c->len; i++) {
    fprintf(ofst, "%d: " , i);
    CoordFigureOut(c->cont[i], ofst);
  }
}
