#include "container.h"

//------------------------------------------------------------------------------
// Вывод содержимого контейнера
void SimpleContainer::Out(std::ofstream &ofst) {
  ofst << "Container contents " << len << " elements.\n";
  for(int i = 0; i < len; i++) {
    ofst << i << ": ";
    cont[i]->Out(ofst);
  }
}
