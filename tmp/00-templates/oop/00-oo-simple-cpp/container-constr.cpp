#include "container.h"

//------------------------------------------------------------------------------
// Инициализация контейнера
SimpleContainer::SimpleContainer(): len(0) { }

//------------------------------------------------------------------------------
// Очистка контейнера от элементов (освобождение памяти)
void SimpleContainer::Clear()
{
  for(int i = 0; i < len; i++)
  {
    delete cont[i];
  }
  len = 0;
}
