//------------------------------------------------------------------------------
// figure-in.cpp - содержит процедуру ввода параметров
// для уже созданной любой фигуры
//------------------------------------------------------------------------------

#include <iostream>
#include "figure.h"

//------------------------------------------------------------------------------
// Необходима подключить информацию обо всех имеющихся
// геометрических фигурах
#include "rectangle.h"
#include "triangle.h"
#include "decorator.h"

//------------------------------------------------------------------------------
// Ввод параметров обобщенной фигуры из стандартного потока ввода
Figure* Figure::In(std::ifstream &ifst) {
  Figure *sp;
  int k;
  ifst >> k;
  switch(k) {
    case 1:
      sp = new Rectangle;
      break;
    case 2:
      sp = new Triangle;
      break;
    default:
      return 0;
  }
  // Предполагается обязательное наличие декоратора
  Decorator* decor = new Decorator(sp);
  decor->InData(ifst);
  return decor;
}
