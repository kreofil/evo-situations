//==============================================================================
// container.cpp - реализации методов контейнера, собранные воедино
//==============================================================================

#include "container.h"

//==============================================================================
// Методы супервизора, используемые для обслуживания клиентов
//==============================================================================

//------------------------------------------------------------------------------
// Инициализация контейнера
Container::Container(): len{0} { }

//------------------------------------------------------------------------------
// Очистка контейнера от элементов (освобождение памяти)
void Container::Clear() {
  for(int i = 0; i < len; i++) {
    delete cont[i];
  }
  len = 0;
}

//------------------------------------------------------------------------------
// Ввод содержимого контейнера
void Container::In(std::ifstream &ifst) {
  while(!ifst.eof()) {
    if((cont[len] = Figure::In(ifst)) != 0) {
      len++;
    }
  }
}

//==============================================================================
// Метод выводв фигур, используемые клиентом, осуществляющим "рисование"
//==============================================================================

//------------------------------------------------------------------------------
// Вывод содержимого контейнера
void Container::Out(std::ofstream &ofst) {
  ofst << "Container contents " << len << " elements.\n";
  for(int i = 0; i < len; i++) {
    ofst << i << ": ";
    cont[i]->Out(ofst);
  }
}

//==============================================================================
// Методы, используемые клиентом, осуществляющим геометрические вычисления
//==============================================================================

//------------------------------------------------------------------------------
// Вывод периметров в поток
void Container::PerimeterOut(std::ofstream &ofst) {
  ofst << "Perimeters of figures:\n";
  for(int i = 0; i < len; i++) {
    ofst << i << ": " << cont[i]->Perimeter() << "\n";
  }
}
//------------------------------------------------------------------------------
// Вывод площадей в поток
void Container::AreaOut(std::ofstream &ofst) {
  ofst << "Areas of figures:\n";
  for(int i = 0; i < len; i++) {
    ofst << i << ": " << cont[i]->Area() << "\n";
  }
}
